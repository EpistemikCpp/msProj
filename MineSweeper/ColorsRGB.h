#ifndef ColorsRGB_H
#define ColorsRGB_H

/*   RGB color values.  */

#define RGB_WHITE			RGB(255, 255, 255)	// White
#define RGB_GRAY_Lt			RGB(192, 192, 192)	// Gray Light
#define RGB_GRAY_Dk			RGB(128, 128, 128)	// Gray Dark
#define RGB_BLACK			RGB(  0,   0,   0)	// Black

#define RGB_RED_Ltr			RGB(255, 192, 192)	// Pink
#define RGB_RED_Lt			RGB(255, 128, 128)	// Peach
#define RGB_RED				RGB(255,   0,   0)  // Red
#define RGB_RED_Dk			RGB(192,   0,   0)
#define RGB_RED_Dkr			RGB(128,   0,   0)

#define RGB_GREEN_Lt		RGB(128, 255, 128)	// Light Green
#define RGB_GREEN			RGB(  0, 255,   0)  // Green
#define RGB_GREEN_Dk		RGB(  0, 192,   0)
#define RGB_GREEN_Dkr		RGB(  0, 128,   0)

#define RGB_BLUE_Lt			RGB(128, 128, 255)  // Light Blue
#define RGB_BLUE			RGB(  0,   0, 255)  // Blue
#define RGB_BLUE_Dk			RGB(  0,   0, 192)
#define RGB_BLUE_Dkr		RGB(  0,   0, 128)


#define RGB_YELLOW			RGB(255, 255,   0)	// Yellow
#define RGB_YELLOW_Lt		RGB(255, 255,   128)
#define RGB_YELLOW_Ltr		RGB(255, 255,   192)

#define RGB_BROWN_Dk		RGB(128,  64,   0)	// Brown Dark
#define RGB_BROWN_Red		RGB(192,  64,   0)

#define RGB_MAGENTA_Lt		RGB(255, 128, 255)	// Light Magenta
#define RGB_PURPLE			RGB(255,   64, 255)  // Purple
#define RGB_MAGENTA			RGB(255,   0, 255)	// Magenta
#define RGB_MAGENTA_Dk		RGB(192,   0, 192)
#define RGB_MAGENTA_Dkr		RGB(128,   0, 128)

#define RGB_AQUA			RGB(  0, 255, 255)  // Aqua
#define RGB_CYAN			RGB(  0, 255, 255)	// Cyan
#define RGB_CYAN_Dk			RGB(  0, 192, 192)
#define RGB_CYAN_Dkr		RGB(  0, 128, 128)

#endif